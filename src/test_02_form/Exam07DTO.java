package test_02_form;

public class Exam07DTO {
	
	private String id;
	private String passwd;
	private String confirm_passwd;
	private String name;
	private String phone;
	private String email;
	private int birthYear;
	
	
	public Exam07DTO() {
		
	}

	
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

	public String getConfirm_passwd() {
		return confirm_passwd;
	}

	public void setConfirm_passwd(String confirm_passwd) {
		this.confirm_passwd = confirm_passwd;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getBirthYear() {
		return birthYear;
	}

	public void setBirthYear(int birthYear) {
		this.birthYear = birthYear;
	}
	
	

}
